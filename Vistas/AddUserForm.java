    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombre = txtNombre.getText();
        String apellidos = txtApellidos.getText();
        String tipoDocumento = (String)cbTipoDocumento.getSelectedItem();
        String documento = txtDocumento.getText();
        String correo = txtCorreo.getText();
        
        
        // validamos que se capture correctamente la info
        
        System.out.println("Nombre: " + nombre + " " + apellidos
                            + ", Documento: " + tipoDocumento + " " + documento
                            + ", Correo: " + correo);
        
        // validamos si los datos ingresados no estan vacios 
        
        if(nombre.isEmpty() || apellidos.isEmpty() || documento.isEmpty() || correo.isEmpty()){
            JOptionPane.showMessageDialog(this, "Faltan campos por diligenciar","Registro",JOptionPane.WARNING_MESSAGE);
            
        }else{
            String query = "INSERT INTO `empleados`( `nombreEmp`, `apellidos`, `tipoDocumento`, `documento`, `correo`) VALUES ('"+ nombre +"','"+ apellidos+ "','"+ tipoDocumento +"','"
                                                    + documento + "','"+ correo +"')";
            
            
            try{
                connection = conexion.getConnection();
                st=connection.createStatement();
                //  el executeUpdate se utuliza para las funciona de crear elminar y actualizar
                st.executeUpdate(query);
                JOptionPane.showMessageDialog(this, "El usuario ha sido registrado");
                
            }catch(SQLException e){
                JOptionPane.showMessageDialog(this, "El usuario no ha sido creado", "Empleados",JOptionPane.ERROR_MESSAGE);
                
            }
            this.dispose();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

